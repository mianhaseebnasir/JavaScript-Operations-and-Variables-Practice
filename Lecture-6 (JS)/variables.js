// Variables(let is used in between of curly braces is called local scope)
let a = prompt("Enter Your Name");
c = "Mr. " + a;
alert(c);
console.log(c);
document.writeln(c);


// Variables(var is used in out of curly braces is called global scope)
var b = prompt("Enter a Gender");
alert(b);
console.log(b);
document.writeln(b);


// Variables(const is used for fixed values)
const d = prompt("Enter Your Age");
alert(d);
console.log(d);
document.writeln(d);